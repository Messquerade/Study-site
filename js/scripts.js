$(document).ready(function () {
  $("#JS").click(function () {
    $("#JS-show").toggle();
    $("#JS-hide").toggle();
  });

  $("#OP").click(function () {
    $("#Operators-show").toggle();
    $("#Operators-hide").toggle();
  });

  $("#JS-text").click(function () {
    $("#JS-show").toggle();
    $("#JS-hide").toggle();
  });

  $("#OP-text").click(function () {
    $("#Operators-show").toggle();
    $("#Operators-hide").toggle();
  });
});